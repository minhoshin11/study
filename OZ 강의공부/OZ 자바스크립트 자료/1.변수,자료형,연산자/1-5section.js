/*
test05. 할당 연산
*/

// 변수 선언 및 초기화
let num = 10;

console.log('초기값: ', num); // 초기값: 10

// += 연산자
num += 5; // num = num + 5
console.log('+= 후의 값: ', num); // += 후의 값: 15

// -= 연산자
num -= 3; // num = num - 3
console.log('-= 후의 값: ', num); // -= 후의 값: 12

// *= 연산자
num *= 2; // num = num * 2
console.log('*= 후의 값: ', num); // *= 후의 값: 24

// /= 연산자
num /= 4; // num = num / 4
console.log('/= 후의 값: ', num); // /= 후의 값: 6

// %= 연산자
num %= 5; // num = num % 5
console.log('%= 후의 값: ', num); // %= 후의 값: 1

console.log("----------------------");
