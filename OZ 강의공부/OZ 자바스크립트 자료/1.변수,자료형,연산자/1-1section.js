// 이름() -> 이 이름을 가진 함수(즉, 메소드) 호출
// 'log()'
// 함수, 즉 메소드(기능, 동작, 행위)

// 구급약통.소화제()
// 구급약통.마데카솔()
// console.log()
// console 도구 상자 안의 'log' 라는 도구 즉, 메소드
// console 객체의 log() 메소드 
// console 객체는 브라우저의 디버깅 콘솔에 접근할 수 있는 메소드를 제공

// 객체는 여러 기능(메소드)와 데이터(속성)을 담는 상자와 같다.
// 이 상자 안에는 도구들(메소드)과 나사와 볼트들(속성, 데이터)들이 있다.
// 우선은 '도구 상자'라고만 이해하고 넘어가자.

// console mdn -> https://developer.mozilla.org/ko/docs/Web/API/console

// console.log() 메시지 출력
console.log("Hello World!");

// console.error() 콘솔에 에러 메시지 출력
console.error("내가 만든 error msg");

// Math.abs()
// Math 도구 상자 안의 'abs'라는 도구 즉, 메소드
// Math 는 수학 관련 객체
// abs() 는 숫자의 절댓값을 반환하는 메소드
console.log(Math.abs(5)); // 5
console.log(Math.abs(-5)); // 5

// Math mdn -> https://developer.mozilla.org/ko/docs/Web/JavaScript/Reference/Global_Objects/Math
